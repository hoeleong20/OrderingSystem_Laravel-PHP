          newValue = el.value,
          newCursorPosition;

        if (newValue.match(ccCVCPattern)) {
          newCursorPosition = ccCVCInputOldCursor + (newValue.length - ccCVCInputOldValue.length);
          el.value = newValue;
        } else {
          el.value = ccCVCInputOldValue;
          newCursorPosition = ccCVCInputOldCursor;
        }

        el.setSelectionRange(newCursorPosition, newCursorPosition);
      };

